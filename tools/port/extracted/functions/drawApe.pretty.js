function drawApe(b, flash) {
  const R = b.r;
  ctx.fillStyle = flash ? '#fff' : '#6a5230';
  ctx.beginPath();
  ctx.ellipse(0, 6, R * 0.95, R * 0.85, 0, 0, 7);
  ctx.fill();
  ctx.fillStyle = flash ? '#fff' : '#c9a24b';
  ctx.beginPath();
  ctx.arc(0, -6, R * 0.72, 0, 7);
  ctx.fill();
  ctx.fillStyle = '#8a6a3a';
  ctx.beginPath();
  ctx.ellipse(0, -2, R * 0.5, R * 0.42, 0, 0, 7);
  ctx.fill();
  ctx.strokeStyle = '#2a1e10';
  ctx.lineWidth = 3;
  ctx.beginPath();
  ctx.moveTo(-R * 0.3, -R * 0.15);
  ctx.lineTo(-R * 0.08, -R * 0.15);
  ctx.moveTo(R * 0.08, -R * 0.15);
  ctx.lineTo(R * 0.3, -R * 0.15);
  ctx.stroke();
  ctx.fillStyle = '#2a1e10';
  circle(-R * 0.19, -R * 0.05, 2.4, '#2a1e10');
  circle(R * 0.19, -R * 0.05, 2.4, '#2a1e10');
  ctx.beginPath();
  ctx.moveTo(-R * 0.18, R * 0.22);
  ctx.quadraticCurveTo(0, R * 0.12, R * 0.18, R * 0.22);
  ctx.stroke();
  circle(-R * 0.7, -6, R * 0.2, flash ? '#fff' : '#c9a24b');
  circle(R * 0.7, -6, R * 0.2, flash ? '#fff' : '#c9a24b');
  ctx.fillStyle = '#c33';
  ctx.beginPath();
  ctx.ellipse(0, -R * 0.7, R * 0.6, R * 0.24, 0, Math.PI, 0);
  ctx.fill();
}
