const { Pool } = require('pg');

function createPool() {
  if (process.env.DATABASE_URL) {
    return new Pool({ connectionString: process.env.DATABASE_URL, max: 10 });
  }
  return new Pool({
    host: process.env.PGHOST || '127.0.0.1',
    port: Number(process.env.PGPORT) || 5433,
    user: process.env.PGUSER || 'killallmumus',
    password: process.env.PGPASSWORD,
    database: process.env.PGDATABASE || 'killallmumus',
    max: 10,
  });
}

/** Apply schema needed for Bobina.moe identity + leaderboard. Idempotent. */
async function migrate(pool) {
  await pool.query(`
    CREATE TABLE IF NOT EXISTS bobina_scores (
      id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      name        TEXT,
      handle      TEXT,
      score       BIGINT,
      kills       INTEGER,
      rank        TEXT,
      mode        TEXT,
      won         INTEGER,
      created_at  TIMESTAMPTZ NOT NULL DEFAULT NOW(),
      outfit      TEXT DEFAULT 'og'
    );
  `);

  // Bobina identity columns on scores (nullable for anonymous / legacy rows)
  await pool.query(`
    ALTER TABLE bobina_scores
      ADD COLUMN IF NOT EXISTS bc_id TEXT,
      ADD COLUMN IF NOT EXISTS bobina_username TEXT,
      ADD COLUMN IF NOT EXISTS x_id TEXT,
      ADD COLUMN IF NOT EXISTS avatar TEXT;
  `);
  await pool.query(`
    CREATE INDEX IF NOT EXISTS bobina_scores_score_idx ON bobina_scores (score DESC);
    CREATE INDEX IF NOT EXISTS bobina_scores_handle_idx ON bobina_scores (handle);
    CREATE INDEX IF NOT EXISTS bobina_scores_bc_id_idx ON bobina_scores (bc_id);
  `);

  // Canonical player records keyed by bc_id
  await pool.query(`
    CREATE TABLE IF NOT EXISTS players (
      bc_id            TEXT PRIMARY KEY,
      cached_username  TEXT,
      cached_avatar    TEXT,
      cached_x_id      TEXT,
      cached_x_username TEXT,
      high_score       BIGINT NOT NULL DEFAULT 0,
      kills            INTEGER NOT NULL DEFAULT 0,
      games_played     INTEGER NOT NULL DEFAULT 0,
      created_at       TIMESTAMPTZ NOT NULL DEFAULT NOW(),
      updated_at       TIMESTAMPTZ NOT NULL DEFAULT NOW()
    );
  `);

  // Short-lived OAuth PKCE state (server-side verifier)
  await pool.query(`
    CREATE TABLE IF NOT EXISTS oauth_states (
      state          TEXT PRIMARY KEY,
      code_verifier  TEXT NOT NULL,
      nonce          TEXT NOT NULL,
      created_at     TIMESTAMPTZ NOT NULL DEFAULT NOW()
    );
  `);
  await pool.query(`
    CREATE TABLE IF NOT EXISTS sessions (
      id           TEXT PRIMARY KEY,
      bc_id        TEXT NOT NULL REFERENCES players(bc_id) ON DELETE CASCADE,
      created_at   TIMESTAMPTZ NOT NULL DEFAULT NOW(),
      expires_at   TIMESTAMPTZ NOT NULL
    );
  `);
  await pool.query(`
    CREATE INDEX IF NOT EXISTS sessions_expires_idx ON sessions (expires_at);
  `);

  // Housekeeping: drop expired oauth states / sessions
  await pool.query(`DELETE FROM oauth_states WHERE created_at < NOW() - INTERVAL '1 hour'`);
  await pool.query(`DELETE FROM sessions WHERE expires_at < NOW()`);
}

module.exports = { createPool, migrate };
