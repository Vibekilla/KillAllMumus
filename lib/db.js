const { Pool } = require('pg');

function createPool() {
  if (process.env.DATABASE_URL) {
    return new Pool({ connectionString: process.env.DATABASE_URL, max: 10 });
  }
  return new Pool({
    host: process.env.PGHOST || '127.0.0.1',
    port: Number(process.env.PGPORT) || 5433,
    user: process.env.PGUSER || 'killallmumus',
    password: process.env.PGPASSWORD,
    database: process.env.PGDATABASE || 'killallmumus',
    max: 10,
  });
}

/** Apply schema needed for Bobina.moe identity + leaderboard. Idempotent. */
async function migrate(pool) {
  await pool.query(`
    CREATE TABLE IF NOT EXISTS bobina_scores (
      id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      name        TEXT,
      handle      TEXT,
      score       BIGINT,
      kills       INTEGER,
      rank        TEXT,
      mode        TEXT,
      won         INTEGER,
      created_at  TIMESTAMPTZ NOT NULL DEFAULT NOW(),
      outfit      TEXT DEFAULT 'og'
    );
  `);

  await pool.query(`
    ALTER TABLE bobina_scores
      ADD COLUMN IF NOT EXISTS bc_id TEXT,
      ADD COLUMN IF NOT EXISTS bobina_username TEXT,
      ADD COLUMN IF NOT EXISTS x_id TEXT,
      ADD COLUMN IF NOT EXISTS avatar TEXT;
  `);
  await pool.query(`
    CREATE INDEX IF NOT EXISTS bobina_scores_score_idx ON bobina_scores (score DESC);
    CREATE INDEX IF NOT EXISTS bobina_scores_handle_idx ON bobina_scores (handle);
    CREATE INDEX IF NOT EXISTS bobina_scores_bc_id_idx ON bobina_scores (bc_id);
  `);

  await pool.query(`
    CREATE TABLE IF NOT EXISTS players (
      bc_id            TEXT PRIMARY KEY,
      cached_username  TEXT,
      cached_avatar    TEXT,
      cached_x_id      TEXT,
      cached_x_username TEXT,
      high_score       BIGINT NOT NULL DEFAULT 0,
      kills            INTEGER NOT NULL DEFAULT 0,
      games_played     INTEGER NOT NULL DEFAULT 0,
      created_at       TIMESTAMPTZ NOT NULL DEFAULT NOW(),
      updated_at       TIMESTAMPTZ NOT NULL DEFAULT NOW()
    );
  `);

  await pool.query(`
    CREATE TABLE IF NOT EXISTS oauth_states (
      state          TEXT PRIMARY KEY,
      code_verifier  TEXT NOT NULL,
      nonce          TEXT NOT NULL,
      claim_handle   TEXT,
      created_at     TIMESTAMPTZ NOT NULL DEFAULT NOW()
    );
  `);
  // Older clusters may lack claim_handle
  await pool.query(`
    ALTER TABLE oauth_states ADD COLUMN IF NOT EXISTS claim_handle TEXT;
  `);

  await pool.query(`
    CREATE TABLE IF NOT EXISTS sessions (
      id           TEXT PRIMARY KEY,
      bc_id        TEXT NOT NULL REFERENCES players(bc_id) ON DELETE CASCADE,
      created_at   TIMESTAMPTZ NOT NULL DEFAULT NOW(),
      expires_at   TIMESTAMPTZ NOT NULL
    );
  `);
  await pool.query(`
    CREATE INDEX IF NOT EXISTS sessions_expires_idx ON sessions (expires_at);
  `);

  await pool.query(`DELETE FROM oauth_states WHERE created_at < NOW() - INTERVAL '1 hour'`);
  await pool.query(`DELETE FROM sessions WHERE expires_at < NOW()`);
}

/**
 * Claim anonymous / X-handle scores under a Bobina account.
 * Keeps the best run per bc_id after merge.
 */
async function claimScoresForPlayer(pool, {
  bcId,
  bobinaUsername,
  avatar,
  xId,
  xUsername,
  claimHandle,
}) {
  const handles = new Set();
  for (const h of [claimHandle, xUsername, bobinaUsername]) {
    if (h) handles.add(String(h).replace(/^@+/, '').toLowerCase());
  }
  if (!handles.size && !bcId) return { claimed: 0 };

  // Attach matching unlinked rows (by X handle or display name)
  const handleList = [...handles];
  if (handleList.length) {
    await pool.query(
      `UPDATE bobina_scores SET
         bc_id = $1,
         bobina_username = COALESCE($2, bobina_username),
         avatar = COALESCE($3, avatar),
         x_id = COALESCE($4, x_id),
         name = COALESCE($5, name)
       WHERE bc_id IS NULL
         AND (
           LOWER(REGEXP_REPLACE(COALESCE(handle,''), '^@+', '')) = ANY($6::text[])
           OR LOWER(REGEXP_REPLACE(COALESCE(name,''), '^@+', '')) = ANY($6::text[])
         )`,
      [
        bcId,
        bobinaUsername || null,
        avatar || null,
        xId || null,
        bobinaUsername ? `@${bobinaUsername}` : null,
        handleList,
      ]
    );
  }

  // Refresh display on all rows already under this bc_id
  await pool.query(
    `UPDATE bobina_scores SET
       bobina_username = COALESCE($2, bobina_username),
       avatar = COALESCE($3, avatar),
       x_id = COALESCE($4, x_id),
       name = COALESCE($5, name),
       handle = COALESCE(NULLIF($6, ''), handle)
     WHERE bc_id = $1`,
    [
      bcId,
      bobinaUsername || null,
      avatar || null,
      xId || null,
      bobinaUsername ? `@${bobinaUsername}` : null,
      xUsername ? String(xUsername).replace(/^@+/, '') : '',
    ]
  );

  // Collapse duplicates for this bc_id — keep highest score only
  await pool.query(
    `DELETE FROM bobina_scores a
     USING bobina_scores b
     WHERE a.bc_id = $1 AND b.bc_id = $1
       AND a.id < b.id
       AND a.score <= b.score`,
    [bcId]
  );
  // If two remain with different scores, keep max
  await pool.query(
    `DELETE FROM bobina_scores
     WHERE bc_id = $1
       AND id NOT IN (
         SELECT id FROM bobina_scores WHERE bc_id = $1 ORDER BY score DESC, id DESC LIMIT 1
       )`,
    [bcId]
  );

  const best = await pool.query(
    `SELECT COALESCE(MAX(score),0)::bigint AS s, COALESCE(MAX(kills),0)::int AS k, COUNT(*)::int AS n
     FROM bobina_scores WHERE bc_id = $1`,
    [bcId]
  );
  const row = best.rows[0];
  await pool.query(
    `UPDATE players SET
       high_score = GREATEST(high_score, $2),
       kills = GREATEST(kills, $3),
       games_played = GREATEST(games_played, $4),
       cached_username = COALESCE($5, cached_username),
       cached_avatar = COALESCE($6, cached_avatar),
       cached_x_id = COALESCE($7, cached_x_id),
       cached_x_username = COALESCE($8, cached_x_username),
       updated_at = NOW()
     WHERE bc_id = $1`,
    [
      bcId,
      row.s,
      row.k,
      row.n,
      bobinaUsername || null,
      avatar || null,
      xId || null,
      xUsername || null,
    ]
  );

  return { claimed: row.n, highScore: Number(row.s) };
}

module.exports = { createPool, migrate, claimScoresForPlayer };
